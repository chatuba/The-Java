package pedro;

/**
 *
 * @author aluno
 */
public class produto {

    /**
     * @return the codproduto
     */
    public int getCodproduto() {
        return codproduto;
    }

    /**
     * @param codproduto the codproduto to set
     */
    public void setCodproduto(int codproduto) {
        this.codproduto = codproduto;
    }

    /**
     * @return the preço
     */
    public int getPreço() {
        return preço;
    }

    /**
     * @param preço the preço to set
     */
    public void setPreço(int preço) {
        this.preço = preço;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the descrição
     */
    public int getDescrição() {
        return descrição;
    }

    /**
     * @param descrição the descrição to set
     */
    public void setDescrição(int descrição) {
        this.descrição = descrição;
    }

    /**
     * @return the estoque
     */
    public int getEstoque() {
        return estoque;
    }

    /**
     * @param estoque the estoque to set
     */
    public void setEstoque(int estoque) {
        this.estoque = estoque;
    }

    /**
     * @return the valorunitário
     */
    public int getValorunitário() {
        return valorunitário;
    }

    /**
     * @param valorunitário the valorunitário to set
     */
    public void setValorunitário(int valorunitário) {
        this.valorunitário = valorunitário;
    }

    /**
     * @return the setor
     */
    public String getSetor() {
        return setor;
    }

    /**
     * @param setor the setor to set
     */
    public void setSetor(String setor) {
        this.setor = setor;
    }

    /**
     * @return the qualidade
     */
    public String getQualidade() {
        return qualidade;
    }

    /**
     * @param qualidade the qualidade to set
     */
    public void setQualidade(String qualidade) {
        this.qualidade = qualidade;
    }
    private int codproduto;
    private int preço;
    private String nome;
    private String marca;
    private int descrição;
    private int estoque;
    private int valorunitário;
    private String setor;
    private String qualidade;
    
}
